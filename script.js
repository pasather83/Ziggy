function buildProductAwareReply(text, matches) {
  const main = matches[0];
  const bullets = main.response.map((line) => `- ${line}`).join("\n");
  const reminder =
    main.category === "sop"
      ? "For anything sensitive, personal, disciplinary, or payroll-related, check with HR before giving it as the final word."
      : "Before promising anything live, just double-check availability, timings, rates, and blackout dates in the current hotel system.";

  return `${getChilledIntro(main.title)}\n\n${bullets}\n\n${reminder}`;
}

function buildDefaultReply(text, matches) {
  if (matches.length) {
    return buildProductAwareReply(text, matches);
  }

  return [
    "I can help with that.",
    "",
    "Best next move: get the guest name or room, what they need, how urgent it is, and who owns the follow-up.",
    `What we know so far: ${text}`,
    "",
    "If it touches safety, access, billing, availability, pricing, or a complaint, check the live system or bring in the duty manager."
  ].join("\n");
}

function generateReply(text) {
  const lowerText = text.toLowerCase();
  const matches = findMatches(text);

  if (lowerText.includes("handover") || lowerText.includes("shift log")) {
    return buildHandover(text);
  }

  if (
    lowerText.includes("complaint") ||
    lowerText.includes("recovery") ||
    lowerText.includes("angry") ||
    lowerText.includes("upset")
  ) {
    return buildRecovery(text);
  }

  if (
    lowerText.includes("concierge") ||
    lowerText.includes("recommend") ||
    lowerText.includes("reservation")
  ) {
    return buildConcierge(text);
  }

  return buildDefaultReply(text, matches);
}

function chooseZiggyReaction(userText, replyText = "") {
  const text = `${userText} ${replyText}`.toLowerCase();
  const sopWords = ["sop", "policy", "leave", "discipline", "disciplinary", "grievance", "education", "allowance", "wellbeing", "reward", "recognition", "hr "];
  const urgentWords = ["urgent", "medical", "safety", "fire", "leak", "flood", "aggressive", "threat", "security", "angry", "upset", "complaint", "recovery"];
  const unclearWords = ["don't have a confident", "unclear", "confused", "not sure", "what do you mean", "missing"];
  const charmWords = ["concierge", "recommend", "reservation", "art tour", "art story", "guest-facing", "reply could sound"];
  const energyWords = ["open sesame", "orange feels", "offer", "pool", "social", "event", "co-working", "rooms", "suite", "sustainability", "art", "breakfast"];
  const doneWords = ["handover", "clean handover", "sorted", "next move", "thumb", "approved"];

  if (unclearWords.some((word) => text.includes(word))) return "confused";
  if (urgentWords.some((word) => text.includes(word))) return "surprised";
  if (sopWords.some((word) => text.includes(word))) return "pondering";
  if (charmWords.some((word) => text.includes(word))) return "wink";
  if (energyWords.some((word) => text.includes(word))) return "energetic";
  if (doneWords.some((word) => text.includes(word))) return "thumbs";
  return "main";
}

function addMessage(role, text, reaction) {
  const message = { role, text };
  if (role === "ziggy") {
    message.reaction = reaction || "main";
  }
  messages.push(message);
  saveMessages();
  renderMessages();
}

function handleUserText(text) {
  const cleanText = text.trim();
  if (!cleanText) return;

  const replyId = ++pendingReplyId;
  addMessage("user", cleanText);
  input.value = "";

  window.setTimeout(() => {
    if (replyId !== pendingReplyId) return;
    const reply = generateReply(cleanText);
    addMessage("ziggy", reply, chooseZiggyReaction(cleanText, reply));
  }, 260);
}

