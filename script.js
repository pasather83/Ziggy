function normalizeMessages(saved) {
  return saved.map((message) => ({
    ...message,
    reaction: message.role === "ziggy" ? message.reaction || "main" : undefined
  }));
}

function loadMessages() {
  try {
    const saved = JSON.parse(localStorage.getItem(storageKey));
    return Array.isArray(saved) && saved.length ? normalizeMessages(saved) : getStarterMessages();
  } catch {
    return getStarterMessages();
  }
}

function saveMessages() {
  localStorage.setItem(storageKey, JSON.stringify(messages));
}

function escapeHtml(value) {
  return value
    .replaceAll("&", "&amp;")
    .replaceAll("<", "&lt;")
    .replaceAll(">", "&gt;")
    .replaceAll('"', "&quot;")
    .replaceAll("'", "&#039;");
}

function renderMessages() {
  messagesEl.innerHTML = messages
    .map((message) => {
      const safeText = escapeHtml(message.text);
      const body = safeText
        .split("\n")
        .map((line) => `<p>${line || "&nbsp;"}</p>`)
        .join("");
      const speaker = message.role === "user" ? "You" : "Ziggy";
      if (message.role === "ziggy") {
        const reaction = ziggyReactions[message.reaction] || ziggyReactions.main;
        return `
          <article class="message-row ziggy-row">
            <img class="ziggy-avatar" src="${reaction.src}" alt="${escapeHtml(reaction.alt)}" loading="lazy" />
            <div class="message ziggy"><strong>${speaker}</strong>${body}</div>
          </article>
        `;
      }
      return `<article class="message-row user-row"><div class="message user"><strong>${speaker}</strong>${body}</div></article>`;
    })
    .join("");
  messagesEl.scrollTop = messagesEl.scrollHeight;
}

