function buildWorkbenchKnowledge(context, category) {
  const mode = category === "sop" ? workbenchModes.sop : workbenchModes.product;
  if (!context.combined) return mode.empty;

  const matches = findCategoryMatches(context.combined, category);
  if (!matches.length) {
    return category === "sop"
      ? "I don’t have a confident SOP match from that wording yet. Try adding the policy area, like leave, discipline, education allowance, wellbeing, or rewards."
      : "I don’t have a confident product match from that wording yet. Try adding the area, like rooms, offers, Open Sesame, Orange Feels, events, co-working, art, or sustainability.";
  }

  const main = matches[0];
  const reminder =
    category === "sop"
      ? "For sensitive, personal, disciplinary, or payroll cases, confirm with HR before giving it as the final word."
      : "For live rates, availability, blackout dates, and booking terms, double-check the current hotel system.";

  return [`${getChilledIntro(main.title)}`, "", bulletLines(main.response.slice(0, 7)), "", reminder].join("\n");
}

