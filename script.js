function scoreKeyword(lowerText, keyword) {
  const lowerKeyword = keyword.toLowerCase();
  if (lowerText === lowerKeyword) return 4;
  if (lowerText.includes(lowerKeyword)) return lowerKeyword.length > 8 ? 3 : 2;
  return 0;
}

function findMatches(text) {
  const lowerText = text.toLowerCase();
  return knowledgeBase
    .map((item) => {
      const score = item.keywords.reduce((total, keyword) => {
        return total + scoreKeyword(lowerText, keyword);
      }, 0);
      return { item, score };
    })
    .filter((match) => match.score > 0)
    .sort((a, b) => b.score - a.score)
    .map((match) => match.item);
}

function findCategoryMatches(text, category) {
  return findMatches(text).filter((item) =>
    category === "sop" ? item.category === "sop" : item.category !== "sop"
  );
}

function detectUrgency(text) {
  const lowerText = text.toLowerCase();
  if (
    ["medical", "safety", "fire", "leak", "flood", "aggressive", "threat", "electric", "security"].some((word) =>
      lowerText.includes(word)
    )
  ) {
    return "Urgent";
  }
  if (["tomorrow", "next shift", "pending", "callback", "follow-up", "follow up"].some((word) => lowerText.includes(word))) {
    return "Handover";
  }
  return toolUrgency.value;
}

function getToolContext() {
  const subject = toolSubject.value.trim();
  const situation = toolSituation.value.trim();
  const combined = [subject, situation].filter(Boolean).join(" - ");
  return {
    subject,
    situation,
    combined,
    urgency: detectUrgency(combined),
    owner: toolOwner.value
  };
}

function bulletLines(lines) {
  return lines.map((line) => `- ${line}`).join("\n");
}

